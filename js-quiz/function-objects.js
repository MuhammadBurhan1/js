// Create the object
const person = {
    name: 'Muhammad Burhan',
    age: 21,
    occupation: 'Software Engineer'
};

// Function to print each property and its value
function printProperties(obj) {
    console.log(obj.name);
    console.log(obj.age);
    console.log(obj.occupation);
}


printProperties(person);

//Output:
//Muhammad Burhan
//21
//Software Engineer
